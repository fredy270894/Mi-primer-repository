package com.example.Datos.dto;

public class Pasatiempos {

	private String comer;
	private String jugar;
	private String dormir;
	private String tiempoVailar;
	
	
	public String getComer() {
		return comer;
	}
	public void setComer(String comer) {
		this.comer = comer;
	}
	public String getJugar() {
		return jugar;
	}
	public void setJugar(String jugar) {
		this.jugar = jugar;
	}
	public String getDormir() {
		return dormir;
	}
	public void setDormir(String dormir) {
		this.dormir = dormir;
	}
	public String getTiempoVailar() {
		return tiempoVailar;
	}
	public void setTiempoVailar(String tiempoVailar) {
		this.tiempoVailar = tiempoVailar;
	}
	
	
}
